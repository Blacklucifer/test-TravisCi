/**
 * Offset when moving Kara.
 *
 * @author Ullrich Hafner
 */
class Offset {
    /**
     * Creates a new instance of {@link Offset}.
     *
     * @param x
     *         horizontal offset
     * @param y
     *         vertical offset
     */
    Offset(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    final int x;
    final int y;
}
